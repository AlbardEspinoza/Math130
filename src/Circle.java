public class Circle {

    //Instance variable
    private double radius;  //Radius of the Circle

    public Circle(double iradius) {
        radius = iradius;
    }

    public double getRadius() {//You're getting the radius value.
        return radius;
    }

    public void setRadius(double nRadius) {//You're setting a new value to radius.
        radius = nRadius;
    }
}
